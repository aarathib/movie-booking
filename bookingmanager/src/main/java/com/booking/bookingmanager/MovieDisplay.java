     public void fillcb1()
     {
     
    try{
            Connection conn=Database.getConnection();
            PreparedStatement st=conn.prepareStatement("select * from showt where (date=? and tname=?) and movieid=? ");
            st.setString(1,dlbl.getText() );
            st.setString(2,ShowDao.getTname(Tnamelbl.getText()));
            st.setInt(3,ShowDao.getMovie(jLabel1.getText()));
            ResultSet rs=st.executeQuery();
            if(rs.next()==false){ 
                dispose();
                JOptionPane.showMessageDialog(null, "No shows currently available");
               
            }else{
            do{
              cb1.addItem(rs.getString("time"));
            }while(rs.next());
               
                
            }
            }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    
    
     }
     private void addimage(String s)
    {
        try{
            Connection conn= Database.getConnection();
            PreparedStatement st=conn.prepareStatement("select image from movie where title=?");
            st.setString(1, s);
            ResultSet rs=st.executeQuery();
            if(rs.next()){
                byte[] img=rs.getBytes("image");
                ImageIcon image=new ImageIcon(img);
                Image im=image.getImage();
                Image myImg=im.getScaledInstance(imglbl.getWidth(),imglbl.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon newImage=new ImageIcon(myImg);
                imglbl.setIcon(newImage);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "no image error");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
