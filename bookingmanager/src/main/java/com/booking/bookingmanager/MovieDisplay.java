    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try{   
            String stime=cb1.getSelectedItem().toString();
            String tname=ShowDao.getTname(Tnamelbl.getText());
            int mname=ShowDao.getMovie(jLabel1.getText());
            Connection conn=Database.getConnection();
            PreparedStatement st=conn.prepareStatement("select * from showt where (date=? and tname=?) and (movieid=? and time=?) ");
            st.setString(1,dlbl.getText() );
            st.setString(2,tname);
            st.setInt(3,mname);
            st.setString(4, stime);
            ResultSet rs=st.executeQuery();
            if(rs.next()){
              int bksts=Integer.parseInt(seatf.getText());
              int tseats=Integer.parseInt(rs.getString("rseats"));
              int sid=Integer.parseInt(rs.getString("showid"));
              if(bksts>tseats){
                  JOptionPane.showMessageDialog(null,"Tickets not available");
              }
              else{
                  
                  String username=usrlbl.getText();
                  
                  new Payment(sid,username,bksts).setVisible(true);
                  
              }
                
            }
            
            }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

