    private void FillLabel(int sid,int bk){
        try{
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/theatresystem","root","password");
            
            PreparedStatement st=conn.prepareStatement("select * from showt where showid=? ");
            st.setString(1,String.valueOf(sid) );
            ResultSet rs=st.executeQuery();
            if(rs.next()){
              Mname.setText(rs.getString("movieid"));
              DateL.setText(rs.getString("date"));
              TimeLabel.setText(rs.getString("time"));
              int p=Integer.parseInt(rs.getString("price"));
               int tp=bk*p;
               TotalA.setText(String.valueOf(tp));
               remseat=Integer.parseInt(rs.getString("rseats"));
               int tseat=Integer.parseInt(rs.getString("tseats"));
              
                conn.close();
            }
            }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    
    }
