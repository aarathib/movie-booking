    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int status=0;
        try{
            Connection con=Database.getConnection();
            PreparedStatement ps=con.prepareStatement("DELETE FROM movie WHERE movieid=?;");
            ps.setInt(1, id);
            status=ps.executeUpdate();
            System.out.println(status);
            if(status>0)
                JOptionPane.showMessageDialog(AdminSuccess.this, "Movie deleted Successfully!!");
            else{
                JOptionPane.showMessageDialog(AdminSuccess.this, "Movie not deleted","", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            e.printStackTrace();
        }finally{
            if(status==0){
                JOptionPane.showMessageDialog(AdminSuccess.this, "Couldn't Delete Movie","", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_deletebtnActionPerformed

