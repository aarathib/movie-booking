    private void fillMovie() {
        try{
            Connection con=Database.getConnection();
            PreparedStatement ps=con.prepareStatement("SELECT title FROM MOVIE;");
            ResultSet rs=ps.executeQuery();
            while(rs.next())
                movielist.addItem(rs.getString("title"));
            con.close();
        }catch(Exception e){System.out.println(e);}
        
    }
    
